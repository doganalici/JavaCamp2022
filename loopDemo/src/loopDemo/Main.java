package loopDemo;

public class Main {

	public static void main(String[] args) {
		  //For
        for (int i=1;i<=10;i+=2){
            System.out.println(i);
        }
        System.out.println("For Döngüsü Bitti\n");

        //While
        int k=1;
        while (k<=10){
            System.out.println(k);
            k+=2;
        }
        System.out.println("While Döngüsü Bitti");

	}

}
